num1 = int(input(print("enter first number")))
num2 = int(input(print("enter second number")))
operation = input(print("enter operation"))

# if operation is + we want to add num1 and num2

if (operation == "+"):
    num3 = num1 + num2
    print(num3)

if (operation == "/"):
    num3 = num1 / num2
    print(num3)

if (operation == "-"):
    num3 = num1 - num2 
    print(num3) 
     
if (operation == "*"):
    num3 = num1 * num2 
    print(num3) 



